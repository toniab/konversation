        <div style="text-align: left; float:left;"> 
            <p>Perhaps more than any other species, human beings are builders by nature.  We're all in the construction business-- but not because we signed up for it.  We don't have any say in the matter:  as beings equipped with few natural talents, we're forced to rely on artificial mechanisms to extend our reach in the world.  These things are both physical and mental:  we don't just build bridges, construct skyscrapers, and manufacture automobilites; we are equally - and perhaps more importantly - builders of an entirely different kind. We're cultural, conceptual constructers, if you will.</p> 
            <p>As artists of world-making, we're bringing new things into existence all of the time- things that require human minds for their making. Things like money, chess, the United States of America, and the Olympics. These aren't just physical objects - their objects whose existence depends on human intentions, beliefs, desires, and volitions.</p> 
        </div>
        
        <div style="text-align: right; font-size: 12px; font-style: italic;">
            <a style="color:#000;" href="http://www.davidhollandartist.com/David-Holland-Torsos.html">
            <img src="https://s3-us-west-2.amazonaws.com/konversation/daniel3pic.jpg" />
            'Vatican Torso Hercules' -David Holland
            </a>
        </div>
        
        <div style="text-align: left; float:left;">
            <p>This mental manufacturing, this brain-based building is somewhat miraculous - so miraculous that it can trick us into believe we - or, at least, someone - has control over the process.  Unlike Picasso plotting the Guernica with meticulous precision, the built human world isn't itself subject to our caprices.  Any artist will tell you that the matter - the raw stuff of the construction process - imposes constraints on the product. Stones are recalcitrant, only sculptable according to particular fault-lines. </p>
        </div>   
        
        <div style="text-align: right; font-size: 12px; font-style: italic;">
            <a style="color:#000;" href="http://www.chiharu-shiota.com/works.html">
            <img src="https://s3-us-west-2.amazonaws.com/konversation/daniel4pic.jpg" />
            'Stairway' -Chihary Shiota 2012 Germany
            </a>
        </div>  

        <div style="text-align: left; float:left;">
            <p>So is the physics of culture: we all start somewhere, with some determinate material situation that is not of our own making.  We're not born we're thrown - plummeting into the world against our will. And it gets worse: not only are we condemned to constantly spin cultural and symbolic webs of significance, we, too, are spun in the process.  There's no "we" that stands outside the process, who could peak over our corporeal shoulders and peer down on the world below.  Human beings aren't like that. There's no being behind the doing: the illusion "self" might be no more than a byproduct of a certain activity of construction.  </p>
        </div>
        
        <div style="text-align: right; font-size: 12px; font-style: italic;">
            <a style="color:#000;" href="http://www.ebay.com/itm/Vtg-Fools-Gold-Iron-Pyrite-Rock-Specimen-/380143221478">
