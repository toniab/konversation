        <div style="text-align: left; float:left;">
            <p>This can be disheartening, for a lot of what we build collectively isn't all it's cracked up to be: </p>
            <p>Glittering like gold, but with a toxic center.  And, worse still, a lot of this stuff doesn't look to us to be "built" at all.  Take the concepts we define our lives by, the things nearest and dearest to us, the conceptual frames in which we house our selves-- concepts like "race," "gender," "sexuality." These things exist in the world a lot more like chess does than like molecules do: their psychological frames we've read into the world - spun in to being.  But they often seem as natural as can be, so close to us, we don't even notice.  Like water to a fish.</p>
        </div>  
        
        <div style="text-align: right; font-size: 12px; font-style: italic;">
            <a style="color:#000;" href="http://www.nada.kth.se/~asa/Abalone/abalone.html">
            <img src="https://s3-us-west-2.amazonaws.com/konversation/daniel6pic.jpg" />
            'Abalone Fractals' -Anders Sandberg 2004
            </a>
        </div>  

        <div style="text-align: left; float:left;">
        <p>And these things are toxic not because they're all bad, but because they're so deeply intertwined as corner stones of oppressive regimes. Take the concept of race. Race - historically - is a political division, not a biological one, a division born of a social and legal system intended at the outset to deny certain privileges to certain groups, and to reenforce the dominance of others. It was constructed - legally and culturally - in the literal sense of the word, and only subsequently came to seem like a self-evident matrix for understanding human difference. That doesn't mean that the concept has no positive social function (it does), or that we should forget about it (we shouldn't).  But it does mean we should be note that race as a political system is far from benign.</p>
        </div>
        
        <div style="text-align: right; font-size: 12px; font-style: italic;">
            <a style="color:#000;" href="http://smithjan.com/blog/2011/04/27/igrushka-a-fable-of-toys-isotopes/">
