        <p>Within 2 years my grandparents were divorced. By the age of 6 my mother had to stand between her parents on a porch, in hopes that her small body would shield her mother from her father's fist. So you see there is what the mother hopes to give her daughter, what the mother has dreamed of giving her daughter, and what life circumstances lead her to share in actuality. My mother had inherited from my grandmother not just the hopes and dreams of a first generation Cali girl, southern born, East Oakland raised, but also the pain and the weight that all of this entailed. This is the same pain and longing that lead her to divorce my father, and the very same pain that she has unwittingly gifted to me.</p>
        
