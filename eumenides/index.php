        <p>I allow my mind to wander often to a future where I am mother. Good and bad, I compare to the relationship with my own. No matter how much I dissect the bond between my mother and I, I am unable to fathom a reality where I am not subject to the same pressures and the same mistakes as the woman that birthed me. I owe so much to her essence, yet I seek so desperately to be different. To refuse the blessings and subsequent curses that come with being her offspring. Her first born. I have watched my mother love. I have seen her fall out of love. I have felt intimately her search for herself.</p>
        
