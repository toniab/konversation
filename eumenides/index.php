        <p>I stand now at 22, moving back into the last place I thought I would be, my mother's home-- on the eve of her second divorce, no less. This marriage produced a second daughter, one like and unlike me in many ways. Strong, sensitive, confident, and delicate all in one. Like me, this 12 year old has inherited a culture that is distant and haunting. A pain and a burden that is not her own. She watches at roughly the same age I was as her parents divorce and her reality folds. Though I know from experience that she will raise from these ashes a phoenix reborn, I wonder why it is that she even had to burn. I wonder why it is that I myself had to burn.</p>
        
