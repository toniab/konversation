        <p>At 8 I watched my mother divorce my father. It was the smoothest separation I had ever heard of. There was no scandal, no one cheated, no one was cruel, there were no transgressions of any kind on either part. It was however a case of lacking. A lacking passed on generation, to generation, woman to woman. My mother was victim to the same disease that now afflicts me, the issue of fantasy and reality. What we intend to give our daughters, what we would like to give our daughters, and what we actually do. My grandmother fought for my mother, literally. She wrapped her arms around my infant mother exhausted on her delivery bed as three nuns tried to pry her from my grandmother's arms.</p> 
        
