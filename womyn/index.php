        <p>My grandma booked my Hooyo a one-way ticket on an old Soviet jet to any so-called first world country Hooyo could receive a decent education and living in; anywhere there was some semblance of equality for women. In the so-called first world, when Hooyo would get her period, she had to cut up a muumuu and use it as a washable menstrual pad because she couldn't afford Always or Kotex. She would wash the pads in a bathtub, until her hands were raw and hang them up to dry on a balcony clothing line.</p>

        <p>I learned about menstruation from Hooyo. I also learned about menstruation at school, when my school made every Somali student meet with the school nurse, a white lady, who told us to wear deodorant and use tampons, assuming that our sweat glands were already open or would open before our peers; that the Somali girls were already menstruating; that we wouldn't or couldn't receive such lectures in our own homes from our own hooyos.</p>

        <p>Muslim girls, or <a style="color:#e885a2"  href="http://www.youtube.com/watch?v=ycFHMhdGuXc&feature=youtu.be&t=2m54s">black girls for that matter</a>, typically don't use tampons. Still, the tampon thing was understandable. Our school had a pool. We would swim five times a week. I swam, at least. The only other Somali girl in my class didn't swim because swimming required parental permission and her parents didn't want her to be like the other girls. <a style="color:#e885a2"  href="http://gawker.com/5953448/privileged-pussy-terrorism-torture-and-american-tomorrows">Girls like me</a>.</p>

        <p>The summer before I started high school, I got my period and Hooyo threw me a party. Hooyo invited my younger sisters and two of her close friends. It looked like a hazing than a menarche rite of passage. Hooyo and her friends, all from Northern Somalia, threw eggs at me in the backyard as a part of an old Somali tradition called foolad, which translates to shower and marks the transition from girlhood to womanhood. The eggs, thrown over my shoulders, signified protection from ‘evil eye.' Hooyo inserted my feet in a bucket of cold water like a lazy baptism, pouring plain popcorn and Hershey's miniature chocolates and money over my head. There's a saying in Somali, raran bay cagaha gelisay, which literally means she put her feet on hot coals. The bucket of water ushers in a smooth coming-of-age, warding off metaphorical hot coals&#8722; trouble&#8722; in a new woman's life.</p>

        <p>Hooyo took many embarrassing photos of me and triplicated the photos. For years every time I would try to throw one away, there would be another one and another one. To Hooyo, every moment of the foolad was significant. However, the ritual felt void of meaning to me— eggs, water, chocolate seemed more like ingredients for a cake, but there was no cake. I didn't know then what I know now, which is that practically all the rituals Somali women perform require things from feminine spaces like the kitchen, the bathroom, the garden. Even when it comes to things like the rampant girl-fighting in towns like Burco, Somalia: peppers to momentarily blind the opponent; petroleum jelly thickly applied on the face to evade scratches; braids with thorns, so if one's hair is yanked, it hurts your opponent, not you.</p>

        <p>From circumcision to menstruation to ritualistic girl-fighting to the consummation of marriage and childbirth to rape and civil war, Somali women shed more blood than the men do, yet we are not remembered for it. Every Somali is expected to know the names of their forefathers, yet most can't tell you who their great grandma is. We are forgotten by our own and remembered in the West as <a style="color:#e885a2;"  href="http://thefeministwire.com/2013/04/poetic-justice-drake-and-east-african-girls/">Young East African Girls</a>&#8722; sex objects; Daughters of Anarchy&#8722; products of perpetual war on TV shows like <em>Degrassi: The Next Generation, The Killing;</em> FGM Memoirists&#8722; victims of our own culture.</p>

        <p>Our narratives don't begin at FGM, at war or in a rap song. Our narratives begin at our hooyos. Our narratives begin in the classroom. Our narratives begin at menstruation. Our narratives begin when we remember ourselves. For me, this remembrance begins everyday and hinges on what I know intrinsically. What I know won't titillate men or confirm stereotypes for white people but it can combat a legacy of forgottenness.</p>
