        <p> i learned in elementary school families grow on trees. i had to put my mother, father, grandparents, cousins, people my parents told me existed all on green paper leaves. One vein down the middle gave it life. Attached to an ugly brown stump with branches I learned to make by drawing a ton of "v's."</p>

        <p> you, yes you, were a little leaf. i don't remember how long ago when you left dance lessons and storytelling as the only memories of you i had. i became a ballerina, i loved to dance so much. but your little leaf grew arms and picked itself off the ugly stump. i think maybe it didn't suit you the way your structured looks do. </p>

        <p>maybe you fell freely towards the ground, maybe you were scared. something blew you away and i never knew where the tumbling took you.</p>

        <p>in middle school i learned families didn't always have green leaves. my father's leaf turned brown and the veins in my leaf grew smaller. i was so dehydrated and tiny, my mother had to take me to the doctor. while an IV sat inside my right vein, the doctor prescribed me Boost bars and Velveta cheese to gain weight. i was still a ballerina.</p>

        <p>in high school i drowned in small sizes and remained unnoticed. i stuck my nose in books and my head mostly dreamed in images i'd never seen before. overachiever with gap sweaters tied around my waist so my womanhood didn't leak and freak people out. i broke up with ballet and it tore my heart apart to know, for my family, art is not life.
    
        <p>in college i learned to love, and i learned to love books even more. i traded satin shoes for essays on foot binding and body modification. somehow fascinated with pushing bodies to the extreme, i never felt like bodies could ever hold all of human potential. my stem was letting loose from the stump and i think you were taking root somewhere</p>

        <p>after college i "abandoned" family for finding my personal potential. i knew you were somewhere nearby and maybe could understand. maybe knew something about art and how it could fit in my life again. just a blip on my radar though, you never responded to my signals.</p>
    
    
        <p>you're pretty now. gorgeous beautiful. i found you in a dark trough all the animals came to feed at. you're a leaf with pearl-lined veins and elegant edges. i burst, remembering when you taught me about peaches.</p>

        <p>looking for the right soil to plant trees in is hard. i think i found yours to be somewhat nourishing. i haven't seen you since that night, but let's get pollenated some time. i think we'd have a blast.</p>

